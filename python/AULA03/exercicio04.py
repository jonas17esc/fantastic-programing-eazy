n1 = int(input("Digite um valor para o numero1 : "))
n2 = int(input("Digite um valor para o numero2 : "))


soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
divisao_exata = n1 // n2
resto_divisao = n1 % n2
potencia = n1 ** n2

print(f"Soma: {soma}")
print(f"Subtracao: {subtracao}")
print(f"Multiplicacao: {multiplicacao}")
print(f"Divisao: {divisao}")
print(f"Divisao_exata: {divisao_exata}")
print(f"Resto_divisao: {resto_divisao}")
print(f"Potencia: {potencia}")




